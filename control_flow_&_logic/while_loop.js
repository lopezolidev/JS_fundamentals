/*
    - While loop does the same as the for loop or the do...while loop
    - Executes a code while a certain condition remains true, if not, the repetition stops
  
    Structure:

    initizaling of iterator variable

    while (condition regarding iterator or other variables) {
    
    code ...
    
    increment of iterator variable if condition depends on it
    }
*/

let i = 0 // initialization of iterator variable

while(i < 10){ // condition depending on iterator variable
    
    console.log(i) // code
    
    i++ // increment of the iterator variable
}

