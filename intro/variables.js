// initializing
let first_variable

// assigning
first_variable = 'a'

// declaring → initialization + assignation 
let second_variable = 'b'

// Allowed and preffered: long and descriptive names
let computer_case = 'computer case'
let LSD_Monitor = 'LSD monitor'
let site_URL = 'https://google.com'

// Bad practice: short, ambiguous and indecipherable names
let a = 34.5643
let bc_AX_9 = 'some variable'

// variables can be declared with 'const'
const pi = 3.1416 //← this variable cannot be changed in the future

console.log(first_variable, second_variable, computer_case, LSD_Monitor, site_URL) // output into console
